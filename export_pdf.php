// --- Initialize FPDF ---
class BlueprintPDF extends FPDF {
    // Page header
    function Header() {
        // Logo / Title area
        $this->SetFont('Arial','B',20);
        $this->SetTextColor(59, 130, 246); // Blue branding
        $this->Cell(0,10,'BLUEPRINT PRO - Financial Intelligence',0,1,'L');
        
        $this->SetFont('Arial','',10);
        $this->SetTextColor(100, 116, 139);
        $this->Cell(0,6,'Decision Validation & Financial Breakdown Report',0,1,'L');
        $this->Ln(4);
        
        // Draw a line
        $this->SetDrawColor(226, 232, 240);
        $this->Line(10, $this->GetY(), 200, $this->GetY());
        $this->Ln(6);
    }

    // Page footer
    function Footer() {
        $this->SetY(-15);
        $this->SetFont('Arial','I',8);
        $this->SetTextColor(148, 163, 184);
        $this->Cell(0,10,'Page '.$this->PageNo().' - Generated by Blueprint Pro System',0,0,'C');
    }
    
    // Helper to draw a KPI box
    function KPIBox($x, $y, $w, $h, $label, $value, $subtext = '', $isPositive = true) {
        $this->SetXY($x, $y);
        
        // Box background & border
        $this->SetFillColor(248, 250, 252);
        $this->SetDrawColor(226, 232, 240);
        $this->Rect($x, $y, $w, $h, 'DF');
        
        // Label
        $this->SetXY($x + 4, $y + 4);
        $this->SetFont('Arial','B',9);
        $this->SetTextColor(71, 85, 105);
        $this->Cell($w - 8, 5, $label, 0, 1, 'C');
        
        // Value
        $this->SetXY($x + 2, $y + 11);
        $this->SetFont('Arial','B',14);
        if ($label == 'Projected Profit/Loss' || $label == 'Variance') {
            if ($isPositive) {
                $this->SetTextColor(16, 185, 129); // Green
            } else {
                $this->SetTextColor(239, 68, 68); // Red
            }
        } else {
            $this->SetTextColor(30, 41, 59); // Dark
        }
        $this->Cell($w - 4, 8, $value, 0, 1, 'C');
        
        // Subtext
        if ($subtext) {
            $this->SetXY($x + 2, $y + 20);
            $this->SetFont('Arial','',8);
            $this->SetTextColor(148, 163, 184);
            $this->Cell($w - 4, 4, $subtext, 0, 1, 'C');
        }
    }
}

