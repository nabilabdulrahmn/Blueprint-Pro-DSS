function printFinRow($pdf, $label, $val, $isBold = false) {
    if ($isBold) {
        $pdf->SetFont('Arial','B',10);
        $pdf->SetTextColor(30, 41, 59);
    } else {
        $pdf->SetFont('Arial','',10);
        $pdf->SetTextColor(71, 85, 105);
    }
    $pdf->Cell(95, 6, $label, 0, 0, 'L');
    $pdf->Cell(95, 6, 'RM ' . number_format($val, 2), 0, 1, 'R');
}

